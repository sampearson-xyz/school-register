    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        foreach($request->all()['attendance'] as $student_id => $mark)
        {
            $al = new AttendanceLesson;

            $al->student_id = $student_id;
            $al->lesson_id = $request->all()['lesson_id'];
            $al->mark = $mark;

            $al->save();
        }

        $lesson = Lesson::find($request->all()['lesson_id']);

        $lesson->status = 'closed';

        $lesson->save();

        return response(redirect('/lesson'));
    }

